# 변수에 자료형 설정 
uVariable = "안녕"	# str	
uVariable = 20	# int	
uVariable = 20.5	# float	
uVariable = 1j	# complex	
uVariable = ["사과", "배", "딸기"]	# list	
uVariable = ("대한민국", "일본", "중국")	# tuple	
uVariable = range(6)	# range	
uVariable = {"name" : "홍길동", "age" : 36}	# dict	
uVariable = {"현대", "기아", "삼성"}	# set	
uVariable = frozenset({"삼성", "LG", "대우"})	# frozenset	
uVariable = True	# bool	
uVariable = b"Hello"	# bytes	
uVariable = bytearray(5)	# bytearray	
uVariable = memoryview(bytes(5)) # memoryview

# 변수에 특정 자료형 설정 
