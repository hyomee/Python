uVariable = int(20)	                                # int	
uVariable = float(20.5)                             # float	
uVariable = complex(1j)                             # complex	
uVariable = list(["사과", "배", "딸기"])             # list	
uVariable = tuple(("대한민국", "일본", "중국"))       # tuple	
uVariable = range(6)	# range	
uVariable = dict({"name" : "홍길동", "age" : 36})	# dict	
uVariable = set({"현대", "기아", "삼성"})	# set	
uVariable = frozenset({"삼성", "LG", "대우"})	    # frozenset	
uVariable = bool(3)	# bool	
uVariable = bytes(64)	# bytes	
uVariable = bytearray(5)	# bytearray	
uVariable = memoryview(bytes(5)) # memoryview

