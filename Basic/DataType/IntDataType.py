# int : 정수 자료형
num01 = 10      # 양수 : 변수 num01에 10 저장 
num02 = -10     # 음수 : 변수 num02에 -10 저장

print(num01)    # num01 변수 값 표시 10
print(num02)    # num02 변수 값 표시 -10

# float : 실수 자료형 
float01 = 10.25     # 양수 : float01에  10.25 저장
float02 = -10.25    # 음수 : float01에  -10.25 저장
float03 = 2e1       # 양수 float03에 2 의 10의 거듭제곱(10)을 계산 하여 저장 
float04 = 2E2       # 양수 float04에 2 의 10의 거듭제곱(100)을 계산 하여 저장 
float05 = -2e3      # 음수 float05에 3 의 10의 거듭제곱(1000)을 계산 하여 저장 
float06 = 10.25e2   # 양수 float06에 2 의 10의 거듭제곱(100)을 계산 하여 저장 
float07 = -10.25e2  # 양수 float07에 2 의 10의 거듭제곱(100)을 계산 하여 저장 

print(float01)      # float01 변수 값 표시 10.25
print(float02)      # float02 변수 값 표시 -10.25
print(float03)      # float03 변수 값 표시 20.0
print(float04)      # float04 변수 값 표시 200.0
print(float05)      # float05 변수 값 표시 -2000.0
print(float06)      # float06 변수 값 표시 1025.0
print(float07)      # float07 변수 값 표시 -1025.0
print(10.25e3)      # 10250.0
print(-10.25e3)     # -10250.0